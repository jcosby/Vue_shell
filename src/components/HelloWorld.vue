<template>
  <div class="hello">
    <h1>Hello World</h1>
  <div>
    Data : {{ example1 }}
  </div>
  <button @click="getLanguage"> Get Language </button>
  <hr>
  </div>
</template>
<script>
import axios from 'axios'
export default {
  name: 'HelloWorld',
  data () {
    return {
      msg: 'Welcome to Your Vue.js App',
      example1: ''
    }
  },
  methods: {
    async getLanguage () {
      try {
        const res = await axios.post(
          'http://localhost:4000/graphql', {
            query: '{ language }'
          })
        this.example1 = res.data.data.language
      } catch (e) {
        console.log('err', e)
      }
    }
  }
}
</script>

